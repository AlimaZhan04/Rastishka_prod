# ---------- helpers ----------
def h1(t): doc.add_heading(t, level=1)
def h2(t): doc.add_heading(t, level=2)
def h3(t): doc.add_heading(t, level=3)


def p(t, bold=False, italic=False, color=None, after=6):
    par = doc.add_paragraph()
    r = par.add_run(t)
    r.bold = bold; r.italic = italic
    if color: r.font.color.rgb = color
    par.paragraph_format.space_after = Pt(after)
    return par


def bullet(t, bold_prefix=None):
    par = doc.add_paragraph(style="List Bullet")
    if bold_prefix:
        r = par.add_run(bold_prefix); r.bold = True
        par.add_run(t)
    else:
        par.add_run(t)
    return par


def num(t):
    return doc.add_paragraph(t, style="List Number")


def set_cell_bg(cell, hexcolor):
    tcPr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:val"), "clear"); shd.set(qn("w:color"), "auto")
    shd.set(qn("w:fill"), hexcolor)
    tcPr.append(shd)


def set_cell_text(cell, text, bold=False, white=False, size=10, align=None):
    cell.text = ""
    par = cell.paragraphs[0]
    if align: par.alignment = align
    par.paragraph_format.space_after = Pt(2)
    par.paragraph_format.space_before = Pt(2)
    for i, line in enumerate(text.split("\n")):
        if i > 0:
            par = cell.add_paragraph(); par.paragraph_format.space_after = Pt(2)
        r = par.add_run(line)
        r.font.size = Pt(size); r.bold = bold
        r.font.name = "Calibri"
        if white: r.font.color.rgb = RGBColor(0xFF, 0xFF, 0xFF)
        else: r.font.color.rgb = INK


def table(headers, rows, widths, header_hex=WINE_HEX, zebra=True):
    t = doc.add_table(rows=1, cols=len(headers))
    t.alignment = WD_TABLE_ALIGNMENT.CENTER
    t.style = "Table Grid"
    t.autofit = False
    hdr = t.rows[0].cells
    for i, htext in enumerate(headers):
        set_cell_text(hdr[i], htext, bold=True, white=True, size=10)
        set_cell_bg(hdr[i], header_hex)
    for ri, row in enumerate(rows):
        cells = t.add_row().cells
        for i, val in enumerate(row):
            set_cell_text(cells[i], val, size=9.5)
            if zebra and ri % 2 == 1:
                set_cell_bg(cells[i], GREY_HEX)
    # ширины
    for row in t.rows:
        for i, c in enumerate(row.cells):
            c.width = Inches(widths[i])
    return t


def add_image(fname, width_in, caption):
    par = doc.add_paragraph(); par.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = par.add_run()
    run.add_picture(os.path.join(ASSETS, fname), width=Inches(width_in))
    cap = doc.add_paragraph(); cap.alignment = WD_ALIGN_PARAGRAPH.CENTER
    rc = cap.add_run(caption); rc.italic = True; rc.font.size = Pt(9.5)
    rc.font.color.rgb = SAGE
    cap.paragraph_format.space_after = Pt(10)


def page_break():
    doc.add_page_break()


def hrule(par):
    pPr = par._p.get_or_add_pPr()
    pbdr = OxmlElement("w:pBdr")
    bottom = OxmlElement("w:bottom")
    bottom.set(qn("w:val"), "single"); bottom.set(qn("w:sz"), "12")
    bottom.set(qn("w:space"), "1"); bottom.set(qn("w:color"), WINE_HEX)
    pbdr.append(bottom); pPr.append(pbdr)


