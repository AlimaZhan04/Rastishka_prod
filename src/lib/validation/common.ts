import { z } from "zod";

/** Имя / ФИО: ≥2 символа, не только цифры/спецсимволы. */
export const nameSchema = z
  .string()
  .trim()
  .min(2, "Минимум 2 символа")
  .max(120, "Слишком длинное значение")
  .refine((v) => /\p{L}/u.test(v), {
    message: "Имя не может состоять только из цифр или символов",
  });

/** Согласие на обработку персональных данных — обязательно. */
export const consentSchema = z.boolean().refine((v) => v === true, {
  message: "Необходимо согласие на обработку персональных данных",
});

/** Текстовое пояснение анкеты — до 200 символов. */
export const note200 = z.string().trim().max(200, "Не более 200 символов");
