import { z } from "zod";

/**
 * Коды мобильных операторов Кыргызстана (для будущего ужесточения проверки).
 * Точный согласованный список — открытый вопрос ТЗ §16. Пока валидация по формату,
 * без жёсткой проверки кода оператора, чтобы не блокировать корректные номера.
 */
export const KG_OPERATOR_CODES = [
  "500", "501", "502", "503", "504", "505", "507", "508", "509", // O!
  "543", "545", "546", "547", // Megacom доп.
  "550", "551", "552", "553", "554", "555", "556", "557", "558", "559", // Megacom
  "600", "601", "602", "603", "606", // Beeline доп.
  "700", "701", "702", "703", "704", "705", "706", "707", "708", "709", // Beeline
  "751", "755", "756", "770", "771", "772", "773", "774", "775", "776", "777", "778", "779", // O!/доп.
  "880", "881", // фикс. моб.
  "990", "995", "996", "997", "998", "999", // прочие
] as const;

/** Сырой ввод нормализуется в формат E.164: +996XXXXXXXXX. */
export const phoneSchema = z
  .string()
  .transform((s) => s.replace(/[^\d+]/g, ""))
  .refine((s) => /^\+996\d{9}$/.test(s), {
    message: "Введите номер в формате +996 XXX XXX XXX",
  });

export function isKnownKgOperator(phoneE164: string): boolean {
  const m = /^\+996(\d{3})\d{6}$/.exec(phoneE164);
  return m ? (KG_OPERATOR_CODES as readonly string[]).includes(m[1]) : false;
}
